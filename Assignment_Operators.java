public class Assignment_Operators {
    public static void main(String[] args){
        int income=30000;
        int bonus=5000;
        //income=income+bonus
        income+=bonus;
        System.out.println(income);
        //income=income-bonus
        income-=bonus;
        System.out.println(income);
        //income=income*bonus
        income*=bonus;
        System.out.println(income);
        //income=income/bonus
        income/=bonus;
        System.out.println(income);
        //income=income%bonus
        income%=bonus;
        System.out.println(income);
        

    }
}
